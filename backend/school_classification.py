def classify_school(name: str, type_str: str = "") -> str:
    """
    对学校进行分类，返回 tier 值。

    分类规则（互斥）：
    - "985": 985 工程院校
    - "211": 仅 211 工程（不含 985）
    - "双一流": 双一流但非 985 也非 211
    - "非双一流": 以上皆非
    """
    if name in SCHOOLS_985:
        return "985"
    if name in SCHOOLS_211:
        return "211"
    if name in SCHOOLS_SHUANGYILIU:
        return "双一流"
    # 通过 type 字段判断（兜底）
    if "双一流" in type_str:
        return "双一流"
    return "非双一流"


def is_taiwan_recognized(name: str) -> bool:
    """判断学校是否被台湾教育部承认"""
    return name in SCHOOLS_TAIWAN_RECOGNIZED
